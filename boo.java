public class boo {
    public static void main(String[] args) {
        boolean p = true, q = false;

        System.out.println("p && q = " + (p && q)); // Logical AND
        System.out.println("p || q = " + (p || q)); // Logical OR
        System.out.println("!p = " + (!p));         // Logical NOT
    }
}
